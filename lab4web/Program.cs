    /// <summary>
    /// класс Program
    /// </summary>
    class Program
    {
        public static View view;
        /// <summary>
        /// обрабатывает полученные данные
        /// </summary>
        /// <param name="company">выбор компании</param>
        /// <param name="service">выбор сервиса</param>
        private static void Request(string company, string service)
        {
            switch (company) /*В зависимости от числа определяется, к какой СУБД нужен доступ.*/
            {
                case "1":
                    Google google = new Google();
                    view = new View(google);
                    break;
                case "2":
                    Yandex yandex = new Yandex();
                    view = new View(yandex);
                    break;
                case "3":
                    Baidu baidu = new Baidu();
                    view = new View(baidu);
                    break;
                default:
                    throw new Exception("Ошибка при выборе компании");
            }
            switch (service) /*В зависимости от числа определяется, к какой команде СУБД нужен доступ.*/
            {
                case "1":
                    view.Search();
                    break;
                case "2":
                    view.Maps();
                    break;
                case "3":
                    view.Photo();
                    break;
                case "4":
                    view.Interpreter();
                    break;
                case "5":
                    view.Calendar();
                    break;
                case "6":
                    view.Cloud();
                    break;
                case "7":
                    view.Video();
                    break;
                case "8":
                    view.Mail();
                    break;
                case "9":
                    view.News();
                    break;
                case "10":
                    view.Weather();
                    break;
                default:
                    Console.WriteLine("Такой команды не существует.");
                    break;
            }

        }

        static void Main(string[] args)
        {
            try
            {
                string company;
                string service;
               
                Console.WriteLine("Введите номер выбранной компании:\r\n" +
                                    "1. Google\r\n" +
                                    "2. Yandex\r\n" +
                                    "3. Baidu");
                company = Console.ReadLine();
                Console.WriteLine("Выберете сервис:\r\n" +
                                    "1. Поиск\r\n" +
                                    "2. Карты\r\n" +
                                    "3. Фото\r\n" +
                                    "4. Переводчик\r\n" +
                                    "5. Календарь\r\n" +
                                    "6. Облако\r\n" +
                                    "7. Видео\r\n" +
                                    "8. Почта\r\n" +
                                    "9. Новости\r\n" +
                                    "10. Погода");
                service = Console.ReadLine();
                Request(company, service);
            }
            catch (Exception ex)
            {
                Console.WriteLine(ex.Message);
            }
            Console.ReadLine();

       
        }
    }
